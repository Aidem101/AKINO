$zip = new ZipArchive();
if ($zip->open($target) !== true) {
    fwrite(STDERR, "Failed to open output document: {$target}\n");
    exit(1);
}

function akino_docx_zip_entry_name(ZipArchive $zip, string $name): string
{
    if ($zip->locateName($name) !== false) {
        return $name;
    }

    $windowsName = str_replace('/', '\\', $name);
    if ($zip->locateName($windowsName) !== false) {
        return $windowsName;
    }

    throw new RuntimeException("ZIP entry not found: {$name}");
}

$documentEntry = akino_docx_zip_entry_name($zip, 'word/document.xml');
$xml = $zip->getFromName($documentEntry);
if ($xml === false) {
    fwrite(STDERR, "Failed to read word/document.xml\n");
    exit(1);
}

$dom = new DOMDocument();
$dom->preserveWhiteSpace = false;
$dom->formatOutput = false;
if (!$dom->loadXML($xml)) {
    fwrite(STDERR, "Failed to parse document.xml\n");
    exit(1);
}

$wNs = 'http://schemas.openxmlformats.org/wordprocessingml/2006/main';
$xpath = new DOMXPath($dom);
$xpath->registerNamespace('w', $wNs);
$body = $xpath->query('/w:document/w:body')->item(0);
if (!$body instanceof DOMElement) {
    fwrite(STDERR, "Document body not found\n");
    exit(1);
}

function paragraph_text(DOMXPath $xpath, DOMNode $node): string
{
    $parts = [];
    foreach ($xpath->query('.//w:t', $node) as $textNode) {
        $parts[] = $textNode->textContent;
    }

    return trim(preg_replace('/\s+/u', ' ', implode('', $parts)) ?? '');
}

function make_paragraph(DOMDocument $dom, string $wNs, ?string $style, string $text, bool $code = false): DOMElement
{
    $p = $dom->createElementNS($wNs, 'w:p');

    $pPr = $dom->createElementNS($wNs, 'w:pPr');
    if ($style !== null) {
        $pStyle = $dom->createElementNS($wNs, 'w:pStyle');
        $pStyle->setAttributeNS($wNs, 'w:val', $style);
        $pPr->appendChild($pStyle);
    }

    if ($code) {
        $ind = $dom->createElementNS($wNs, 'w:ind');
        $ind->setAttributeNS($wNs, 'w:left', '720');
        $pPr->appendChild($ind);
    }

    $p->appendChild($pPr);

    $r = $dom->createElementNS($wNs, 'w:r');
    if ($code) {
        $rPr = $dom->createElementNS($wNs, 'w:rPr');
        $fonts = $dom->createElementNS($wNs, 'w:rFonts');
        $fonts->setAttributeNS($wNs, 'w:ascii', 'Consolas');
        $fonts->setAttributeNS($wNs, 'w:hAnsi', 'Consolas');
        $fonts->setAttributeNS($wNs, 'w:cs', 'Consolas');
        $rPr->appendChild($fonts);

        $size = $dom->createElementNS($wNs, 'w:sz');
        $size->setAttributeNS($wNs, 'w:val', '18');
        $rPr->appendChild($size);

        $r->appendChild($rPr);
    }

    $t = $dom->createElementNS($wNs, 'w:t');
    $t->setAttribute('xml:space', 'preserve');
    $t->appendChild($dom->createTextNode($text));
    $r->appendChild($t);
    $p->appendChild($r);

    return $p;
}

$children = [];
foreach ($body->childNodes as $child) {
    $children[] = $child;
}

$startNode = null;
$endNode = null;
foreach ($children as $child) {
    if (!$child instanceof DOMElement || $child->localName !== 'p') {
        continue;
    }

    $text = paragraph_text($xpath, $child);
    if ($startNode === null && str_starts_with($text, 'Глава 2. Проектирование')) {
        $startNode = $child;
        continue;
    }

    if ($startNode !== null && $text === 'ЗАКЛЮЧЕНИЕ') {
        $endNode = $child;
        break;
    }
}

if (!$startNode instanceof DOMElement || !$endNode instanceof DOMElement) {
    fwrite(STDERR, "Could not find chapter replacement boundaries\n");
    exit(1);
}

$node = $startNode;
while ($node !== null && $node !== $endNode) {
    $next = $node->nextSibling;
    $body->removeChild($node);
    $node = $next;
}

